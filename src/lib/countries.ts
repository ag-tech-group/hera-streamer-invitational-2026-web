export interface Country {
  /** Lowercase ISO 3166-1 alpha-2 code (the flag-icons key). */
  code: string
  /** Localized display name from `Intl.DisplayNames`. */
  name: string
}

/**
 * Builds the localized, name-sorted country list for `locale`. Any code the
 * platform can't name (it returns the code back unchanged) is dropped, so the
 * list never shows a bare code.
 */
export function getCountries(locale: string): Country[] {
  const display = new Intl.DisplayNames([locale], { type: "region" })
  return COUNTRY_CODES.flatMap((code) => {
    const name = display.of(code.toUpperCase())
    return name && name.toLowerCase() !== code ? [{ code, name }] : []
  }).sort((a, b) => a.name.localeCompare(b.name, locale))
}

/**
 * Filters `countries` by a free-text query, matching the display name (any
 * position, case-insensitive) or the country code. An empty query returns the
 * full list unchanged.
 */
export function filterCountries(
  countries: Country[],
  query: string
): Country[] {
  const q = query.trim().toLowerCase()
  if (!q) return countries
  return countries.filter(
    (country) =>
      country.name.toLowerCase().includes(q) || country.code.includes(q)
  )
}
